class Motor {
  final String id;
  final String image;
  final String type;
  final String description;
  final int price;
  final int quantity;
  final bool status;

  Motor({
    required this.id,
    required this.image,
    required this.type,
    required this.description,
    required this.price,
    required this.quantity,
    required this.status,
  });
}
